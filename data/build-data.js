const PERIODOS = ["vespertino/noturno","integral","matutino","vespertino","noturno","diurno"];

function derivar(rotulo) {
  for (const p of PERIODOS) {
    if (rotulo.toLowerCase().endsWith(" - " + p)) {
      return { curso: rotulo.slice(0, rotulo.length - (p.length + 3)).trim(), periodo: p };
    }
  }
  return { curso: rotulo, periodo: "" };
}

const cursos = rows.map(([id, rotulo, cidade, srvebp, su]) => {
  const { curso, periodo } = derivar(rotulo);
  return {
    id,
    curso,
    periodo,
    rotulo,
    cidade,
    notas_corte: { SU: su, SRVEBP: srvebp },
    especial: rotulo === "Treineiro" || cidade === "",
  };
});

